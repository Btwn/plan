ALTER FUNCTION dbo.fnPOSCheckSumSerie (
@ID		varchar(36),
@IDL	varchar(36)
)
RETURNS int

AS
BEGIN
DECLARE
@Resultado  int
SELECT @Resultado = CHECKSUM(IDL, ID, RenglonID, Articulo, SubCuenta, SerieLote)
FROM POSLPorCobrarSerieLote WHERE ID = @ID AND IDL = @IDL
RETURN (@Resultado)
END

