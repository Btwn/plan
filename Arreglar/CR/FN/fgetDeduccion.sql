ALTER FUNCTION fgetDeduccion (@MOI as Float, @porcentaje as Float, @meses_uso as int)
RETURNS Float

AS BEGIN
DECLARE @Deduccion as Float
SET @Deduccion =  ((CAST(@MOI AS FLOAT) * @porcentaje / CAST(100 AS FLOAT) )/12) * CAST(@meses_uso AS FLOAT)
