ALTER FUNCTION fnSincroISTablaSinonimo
(
@Tabla					varchar(50)
)
RETURNS varchar(50)

AS BEGIN
DECLARE
@Resultado	varchar(50)
SET @Resultado = NULL
SELECT @Resultado = SinonimoTabla FROM SincroISTablaSinonimo WHERE Tabla = @Tabla
SET @Resultado = RTRIM(ISNULL(@Resultado,@Tabla))
RETURN (@Resultado)
END

