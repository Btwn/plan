RETURNS varchar(20)

AS BEGIN
DECLARE
@Resultado	varchar(20)
SELECT @Resultado = NULL
IF NULLIF(RTRIM(@Almacen), '') IS NOT NULL AND NULLIF(RTRIM(@Tarima), '') IS NOT NULL
IF (SELECT ISNULL(WMS, 0) FROM Alm WHERE Almacen = @Almacen) = 1 SELECT @Resultado = @Tarima
