ALTER FUNCTION [dbo].[fnImporteSimilar] (
	@Importe1 MONEY
   ,@Importe2 MONEY
	)
RETURNS BIT
AS
BEGIN
	DECLARE
		@Resultado BIT
	SELECT @Resultado = 0

	IF (ROUND(@Importe1, 2) = ROUND(@Importe2, 2))
		SELECT @Resultado = 1

	RETURN (@Resultado)
END

