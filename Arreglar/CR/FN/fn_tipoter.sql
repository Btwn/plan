ALTER FUNCTION fn_tipoter (@tipo_tercero varchar(50))
RETURNS varchar(10)
AS BEGIN
RETURN
CASE LOWER(@tipo_tercero)
WHEN 'nacional'	THEN '04'
WHEN 'extranjero' THEN '05'
WHEN 'global'		THEN '15'
ELSE NULL
END
END

