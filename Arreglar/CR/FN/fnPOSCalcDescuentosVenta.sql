ALTER FUNCTION dbo.fnPOSCalcDescuentosVenta (
@DescuentoGlobal   float,
@DescuentoLineal   float
)
RETURNS float

AS
BEGIN
DECLARE
@Resultado  float
SELECT  @Resultado =ROUND(100 - ((100-ISNULL(@DescuentoGlobal,0.0))/100 * (100-ISNULL(@DescuentoLineal,0.0))/100) * 100, 10)
RETURN (@Resultado)
END

