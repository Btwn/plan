ALTER FUNCTION fnASCII_CP437
(
@Caracter				char(1),
@ConvertirCP437			bit = 0
)
RETURNS int

AS BEGIN
DECLARE
@ASCII	int
IF @ConvertirCP437 = 1
SET @ASCII = ASCII(@Caracter COLLATE SQL_Latin1_General_CP437_CI_AS)
ELSE
SET @ASCII = ASCII(@Caracter)
RETURN (@ASCII)
END

