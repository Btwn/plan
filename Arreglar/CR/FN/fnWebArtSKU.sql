ALTER FUNCTION dbo.fnWebArtSKU
(
@SKU        varchar(255)
)
RETURNS varchar(255)

AS BEGIN
DECLARE
@Longitud    int,
@Posicion    int,
@Resultado   varchar(255)
SELECT @Longitud = LEN(@SKU)
SELECT @Posicion = CHARINDEX('#',@SKU,1)
IF   @Posicion > 0
SELECT @Resultado = SUBSTRING(@SKU,@Posicion +1,@Longitud)
ELSE
SELECT @Resultado = @SKU
RETURN (@Resultado)
END

