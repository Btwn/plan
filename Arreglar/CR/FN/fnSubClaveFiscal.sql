ALTER FUNCTION fnSubClaveFiscal (@Clave varchar(20))
RETURNS int

AS BEGIN
DECLARE
@Resultado	int
SELECT @Resultado = NULL
IF NULLIF(RTRIM(@Clave), '') IS NOT NULL
BEGIN
IF @Clave LIKE '%/F2' SELECT @Resultado = 2 ELSE
IF @Clave LIKE '%/F'  SELECT @Resultado = 1
END
RETURN(@Resultado)
END

