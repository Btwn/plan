ALTER PROCEDURE spMonederoFormaAnexa
@Codigo		varchar(50),
@ID			Int,
@Empresa	varchar(5)

AS
BEGIN
DECLARE
@Redime		Bit,
@Forma		Varchar(50),
@Ok			Int
SELECT @Forma = NULL
SET @Redime = 1
IF @Redime = 1
BEGIN
SELECT @Forma = Objeto FROM FormaCobroMon WITH (NOLOCK)
WHERE Empresa = @Empresa AND FormaCobro = @Codigo
END
SELECT @Forma
END

