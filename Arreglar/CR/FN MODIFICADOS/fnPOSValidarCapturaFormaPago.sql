ALTER FUNCTION dbo.fnPOSValidarCapturaFormaPago (
@Codigo		varchar(15)
)
RETURNS bit

AS
BEGIN
DECLARE
@Resultado  bit
SET @Resultado = 1
IF ISNULL(@Codigo,'') <> ''
IF NOT EXISTS(SELECT * FROM CB WITH(NOLOCK) WHERE Codigo = @Codigo AND TipoCuenta = 'Forma Pago' )
SELECT @Resultado = 0
RETURN (@Resultado)
END

