ALTER FUNCTION fnModuloNombre
(
@Modulo				varchar(5)
)
RETURNS varchar(100)

AS BEGIN
DECLARE
@Resultado	varchar(100)
SELECT @Resultado = RTRIM(Nombre) FROM Modulo WITH(NOLOCK) WHERE Modulo = RTRIM(@Modulo)
RETURN (@Resultado)
END

