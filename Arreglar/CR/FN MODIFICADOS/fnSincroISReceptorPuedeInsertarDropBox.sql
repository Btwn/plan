ALTER FUNCTION fnSincroISReceptorPuedeInsertarDropBox(
@ID				int,
@Conversacion	uniqueidentifier)
RETURNS bit
AS
BEGIN
DECLARE @Valor		bit,
@IDInserta	int
SELECT @IDInserta = MIN(IntelisisServiceID)
FROM SincroISDropBoxPaquete WITH(NOLOCK)
WHERE Estatus = 'PENDIENTE'
AND Conversacion = @Conversacion
IF @IDInserta = @ID
SELECT @Valor = 1
ELSE
SELECT @Valor = 0
RETURN @Valor
END

