ALTER function [dbo].[fnAspelDecimales](@Numero varchar(20))
RETURNS Int
AS
BEGIN
DECLARE
@Posi		int,
@Cadena		varchar(200),
@Decimales	int
SET @Cadena = @Numero
SET @Posi = CHARINDEX('.', @Cadena)
IF @Posi = 0
SET @Decimales = 0
ELSE
BEGIN
SET @Decimales = LEN( @Cadena) - @Posi
END
RETURN @Decimales
END

