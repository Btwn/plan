ALTER FUNCTION dbo.fnPOSRedondeoMonetarios (
@Empresa  varchar(5)
)
RETURNS int

AS
BEGIN
DECLARE
@Resultado  int
SELECT @Resultado = RedondeosMonetarios
FROM POSCfg WITH(NOLOCK)
WHERE Empresa = @Empresa
SELECT @Resultado = ISNULL(@Resultado,2)
RETURN (@Resultado)
END

