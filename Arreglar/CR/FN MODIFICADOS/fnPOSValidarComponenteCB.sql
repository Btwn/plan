ALTER FUNCTION dbo.fnPOSValidarComponenteCB (
@ID				varchar(36),
@Estacion		int,
@RenglonID		int
)
RETURNS int

AS
BEGIN
DECLARE
@Resultado  int
SET @Resultado = 0
IF EXISTS( SELECT * FROM POSArtJuegoComponente WITH(NOLOCK) WHERE   RID = @ID  AND Estacion = @Estacion AND RenglonID = @RenglonID
AND NULLIF(Opcion,'') IS  NULL AND Juego IS NOT  NULL)
SELECT @Resultado = 1
RETURN (@Resultado)
END

