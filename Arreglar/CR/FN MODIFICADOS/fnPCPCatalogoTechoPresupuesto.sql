ALTER FUNCTION fnPCPCatalogoTechoPresupuesto
(
@Proyecto				varchar(50),
@Clave					varchar(20)
)
RETURNS money

AS BEGIN
DECLARE
@Resultado	money
SELECT @Resultado = ISNULL(TechoPresupuesto,0.0) FROM ClavePresupuestalCatalogo WITH(NOLOCK) WHERE Clave = @Clave AND Proyecto = @Proyecto
RETURN (@Resultado)
END

