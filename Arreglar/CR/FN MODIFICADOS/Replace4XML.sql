ALTER FUNCTION [dbo].[Replace4XML](@Value varchar(max))
RETURNS varchar(max)
AS
BEGIN
DECLARE @ToReplace TABLE(Valor char)
INSERT @ToReplace
SELECT item
FROM dbo.fnSplit('(,),", ,<,>,%,/,\,;,#,$,&,:', ',')
SELECT @Value = REPLACE(@Value, Valor, '')
FROM @ToReplace
RETURN @Value
END

