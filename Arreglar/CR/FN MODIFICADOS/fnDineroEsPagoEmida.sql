ALTER FUNCTION fnDineroEsPagoEmida(
@ModuloID		int)
RETURNS int
AS
BEGIN
DECLARE @Valor	int
SELECT @Valor = COUNT(c.ID)
FROM DineroD Dd WITH(NOLOCK)
JOIN Dinero D WITH(NOLOCK) ON Dd.Aplica = D.Mov AND Dd.AplicaID = D.MovID
JOIN MovImpuesto m WITH(NOLOCK) ON m.Modulo = 'DIN' AND m.ModuloID = D.ID AND m.OrigenModulo = 'CXP'
JOIN Cxp c WITH(NOLOCK) ON m.OrigenModuloID = c.ID
JOIN MovTipo mt WITH(NOLOCK) ON c.Mov = mt.Mov AND mt.Modulo = 'CXP' AND ISNULL(mt.SubClave, '') = 'CXP.AEMIDA'
WHERE Dd.ID = @ModuloID
RETURN @Valor
END

