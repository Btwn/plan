ALTER PROCEDURE spConceptoAPP
@Modulo   char(5),
@Concepto varchar(50) OUTPUT

AS BEGIN
SELECT @Concepto = ISNULL(ConceptoAPP.Concepto, @Concepto)
FROM ConceptoAPP
WHERE ConceptoAPP.Modulo       = @Modulo
AND ConceptoAPP.ConceptoAPP  = @Concepto
END
RETURN

