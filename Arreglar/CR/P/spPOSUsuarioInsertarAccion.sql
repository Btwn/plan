ALTER PROCEDURE spPOSUsuarioInsertarAccion
@Estacion     int,
@Usuario      varchar(10)

AS
BEGIN
INSERT POSUsuarioAccion (
Usuario, Accion)
SELECT
Usuario, Accion
FROM POSUsuarioAccionTemp
WHERE Estacion = @Estacion
AND ID IN(SELECT ID FROM ListaID  WHERE Estacion = @Estacion)
END

