ALTER PROCEDURE spVerCamposTabla
@Tabla	char(100)

AS BEGIN
SELECT "Campo"= c.name
FROM sysobjects o, syscolumns c
WHERE c.id = o.id
AND o.id = object_id(@Tabla)
AND o.sysstat & 0xf = 3
AND c.name not LIKE 'Logico[0-9]'
END

