ALTER PROCEDURE spSAUXMovConsumoDirecto
@ConsumoDirecto		bit

AS BEGIN
IF @ConsumoDirecto = 0
SELECT Mov FROM MovTipo WHERE Modulo = 'Inv' AND Clave = 'INV.SM' AND SubClave = 'INV.SAUX'
ELSE
SELECT Mov FROM MovTipo WHERE Modulo = 'Inv' AND Clave = 'INV.CM' AND SubClave = 'INV.SAUX'
RETURN
END

