ALTER PROCEDURE spCfgRegHist

AS BEGIN
DECLARE
@Tabla	varchar(100),
@Estatus	varchar(15)
DECLARE crCfgRegHist CURSOR LOCAL FOR
SELECT SysTabla, Estatus
FROM CfgRegHist
ORDER BY SysTabla
OPEN crCfgRegHist
FETCH NEXT FROM crCfgRegHist INTO @Tabla, @Estatus
WHILE @@FETCH_STATUS <> -1
BEGIN
IF @@FETCH_STATUS <> -2
BEGIN
EXEC spRegHistTrigger @Tabla, @Estatus
END
FETCH NEXT FROM crCfgRegHist INTO @Tabla, @Estatus
END  
CLOSE crCfgRegHist
DEALLOCATE crCfgRegHist
RETURN
END

