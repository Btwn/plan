ALTER PROCEDURE spSabanaActualizarTraspaso
@Estacion           INT,
@Articulo           VARCHAR(20),
@Almacen            VARCHAR(20),
@Valor              VARCHAR(2)

AS
BEGIN
UPDATE SabanaD SET Traspaso =  @Valor WHERE Estacion = @Estacion AND Articulo = @Articulo AND Almacen = @Almacen
END

