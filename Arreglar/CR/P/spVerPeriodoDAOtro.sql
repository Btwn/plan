ALTER PROCEDURE spVerPeriodoDAOtro
@PeriodoTipo	varchar(50),
@Fecha		datetime,
@Empresa	varchar(5)	= NULL

AS BEGIN
DECLARE
@FechaD datetime,
@FechaA datetime
EXEC spPeriodoDAOtro @PeriodoTipo, @Fecha, @FechaD OUTPUT, @FechaA OUTPUT, @Empresa
SELECT 'FechaD' = @FechaD, 'FechaA' = @FechaA
RETURN
END

