ALTER PROCEDURE spRecArtRuta
@Empresa      varchar(5),
@Articulo     varchar(20)

AS BEGIN
SET NOCOUNT ON
DECLARE @Rama varchar(5)
DECLARE @Tipo varchar(10)
SET @Rama = 'INV'
SET @Tipo = 'Imagen'
IF EXISTS (SELECT TOP 1 Empresa FROM Empresa WHERE Empresa = @Empresa)
BEGIN
SELECT TOP 1 LTRIM(RTRIM(Direccion))
FROM AnexoCta
WHERE Rama = @Rama
AND Tipo = @Tipo
AND Cuenta = @Articulo
ORDER BY Orden
END
END

