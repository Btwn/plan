ALTER PROCEDURE spVentaAF
@ID			AS INT,
@RID		AS INT

AS
BEGIN
SELECT af.AdquisicionValor
FROM SerieLoteMov sl
JOIN ActivoF af ON af.Articulo=sl.Articulo AND af.Serie=sl.SerieLote
WHERE sl.Modulo='VTAS' AND sl.ID=@ID AND sl.RenglonID=@RID
END

