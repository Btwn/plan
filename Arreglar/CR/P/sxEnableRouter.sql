ALTER PROCEDURE sxEnableRouter
@from VARCHAR(10),
@to VARCHAR(10)
AS
BEGIN
DECLARE @router_id VARCHAR(25)
SELECT @router_id = 'rt_'+@from+'_'+@to
UPDATE sx_router SET sync_on_update = 1, sync_on_insert = 1, sync_on_delete = 1 WHERE router_id = @router_id
END

