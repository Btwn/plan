ALTER PROCEDURE spCancelaTiempoExtra
@Empresa	varchar(5),
@Mov       varchar(20),
@MovID     varchar(20)

AS BEGIN
IF RTRIM(LTRIM(@Mov)) = 'Corte'
BEGIN
UPDATE Nomina
SET Estatus  = 'CANCELADO'
WHERE Empresa  = @Empresa
AND Origen   = @Mov
AND OrigenID = @MovID
END
END

