ALTER PROCEDURE spCambiaEstatus
@Personal	varchar(10),
@Fecha		datetime,
@Curso		varchar(50),
@IDExamen	int,
@NPregunta	int

AS
BEGIN
UPDATE heExamenPersonal SET Estatus='CONCLUIDO'
WHERE IDExamen=@IDExamen AND Personal=@Personal AND @Curso=Curso AND Fecha=@Fecha AND NPregunta=@NPregunta
END

