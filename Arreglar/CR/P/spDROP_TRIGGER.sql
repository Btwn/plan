ALTER PROCEDURE spDROP_TRIGGER
@Trigger	sysname,
@Version	int	 = NULL

AS BEGIN
DECLARE
@SQL	varchar(255)
IF @Version IS NOT NULL
IF (select version from version)>@Version
RETURN
IF EXISTS(select * from sysobjects where id = object_id(@Trigger) and sysstat & 0xf = 8)
BEGIN
SELECT @SQL = 'DROP TRIGGER '+@Trigger
EXEC(@SQL)
END
RETURN
END

